public class SumaParametros{
 public static void main(String lai[]){ 

      System.out.println(Integer.parseInt(lai[0]) + Integer.parseInt(lai[1]));
      System.out.println(Integer.parseInt(lai[0]) - Integer.parseInt(lai[1]));
      System.out.println(Integer.parseInt(lai[0]) * Integer.parseInt(lai[1]));
      System.out.println(Integer.parseInt(lai[0]) / Integer.parseInt(lai[1]));
    }
}
